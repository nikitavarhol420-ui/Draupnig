"""Мемные фразы бота в одном месте — вариативные тексты напоминаний и поощрений.

Бот берёт СЛУЧАЙНУЮ фразу из нужного списка (random.choice), поэтому от раза
к разу текст меняется и не приедается. Чтобы добавить вариант — просто допиши
строку в нужный список ниже. Чем больше фраз — тем разнообразнее бот.

Списки:
  TWO_DAYS — приходит за 2 дня до дедлайна
  ONE_DAY  — за 1 день до дедлайна (картинка/голос те же, что у TWO_DAYS)
  TODAY    — в день дедлайна (шлётся дважды: утром и вечером)
  OVERDUE  — дедлайн просран
  DONE     — поощрение, когда задачу закрыли
"""

import random

# --- За 2 дня до дедлайна -------------------------------------------------
TWO_DAYS = [
    "Привет здоровяяк! Еще 2 дня. ММммм",
    "мммм оаоа",
    "Чай будешь?",
]

# --- За 1 день до дедлайна (та же мордочка/голос, что за 2 дня) -----------
ONE_DAY = [
    "Эммм, ну э о как бы",
    "Нормалдаки или плаки плаки?",
]

# --- В день дедлайна ------------------------------------------------------
TODAY = [
    "дедлайн сегодня.",
    "У тЕбЯ ПоЧтИи ПолУчаЕтсяя =}",
]

# --- Дедлайн просран (шлётся каждый день, пока не закрыта) ----------------
OVERDUE = [
    "💀💀💀",
    "ну плиииз",
    "ну можешь не делать уже в целом",
    "не ну мы подождем, а че еще делать",
]

# --- Поощрение: задача закрыта --------------------------------------------
DONE = [
    "YESS YEEESSSS YEEEAH",
]


def pick(pool: list[str]) -> str:
    """Случайная фраза из пула — вариативность от раза к разу."""
    return random.choice(pool)
